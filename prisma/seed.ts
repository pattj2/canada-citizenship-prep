// ─────────────────────────────────────────────
// Main seed function
// ─────────────────────────────────────────────
async function main() {
  const adminUsername = process.env.ADMIN_USERNAME ?? 'admin'

  // Idempotency check
  const topicCount = await prisma.topic.count()
  if (topicCount > 0) {
    console.log('✅ Database already seeded — skipping.')
    return
  }

  console.log('🌱 Seeding topics...')
  const createdTopics = await Promise.all(
    topicsData.map((t) =>
      prisma.topic.upsert({
        where: { slug: t.slug },
        update: {},
        create: t,
      })
    )
  )
  const topicMap = Object.fromEntries(createdTopics.map((t) => [t.slug, t.id]))
  console.log(`   ✓ ${createdTopics.length} topics created`)

  console.log('🌱 Seeding questions...')
  let questionCount = 0
  for (const q of questionsData) {
    const topicId = topicMap[q.topicSlug]
    if (!topicId) {
      console.warn(`  ⚠ Topic not found for slug: ${q.topicSlug}`)
      continue
    }
    await prisma.question.create({
      data: {
        topicId,
        text: q.text,
        type: q.type,
        difficulty: q.difficulty,
        explanation: q.explanation,
        sourceRef: q.sourceRef,
        answers: {
          create: q.answers,
        },
      },
    })
    questionCount++
  }
  console.log(`   ✓ ${questionCount} questions created`)

  console.log('🌱 Seeding leaders...')
  for (const leader of leadersData) {
    await prisma.leader.create({ data: leader })
  }
  console.log(`   ✓ ${leadersData.length} leaders created`)

  console.log('🌱 Seeding admin user...')
  await prisma.user.upsert({
    where: { username: adminUsername },
    update: { role: 'ADMIN' },
    create: { username: adminUsername, role: 'ADMIN' },
  })
  console.log(`   ✓ Admin user '${adminUsername}' created`)

  console.log('\n🍁 Seed complete!')
}

main()
  .catch((e) => {
    console.error('❌ Seed failed:', e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
